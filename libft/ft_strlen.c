#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] && str[i + 1] && str[i + 2] && str[i + 3])
		i += 4;
	while (str[i])
		i++;
	return (i);
}
