#include "libft.h"
#include <stdio.h>

int	ft_lstsize(t_list *lst)
{
	size_t	i;

	if (!lst || lst == NULL)
		return (0);
	i = 0;
	while (lst->next != NULL)
	{
		lst = lst->next;
		i++;
	}
	return (i + 1);
}
